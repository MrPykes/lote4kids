                if ($query->have_posts()) {
                    while ($query->have_posts()) {
                        $query->the_post();
                        if (get_post_meta(get_the_ID(), 'views', true)) {
                            $views += get_post_meta(get_the_ID(), 'views', true);
                        }
                    }
                }
                $terms[$key]->views = $views;
            }
            function cmp($a, $b)
            {
                return $a->views < $b->views;
            }
            usort($terms, "cmp");
