function lote_activity_log($args = array(), $alert_id = 0)
{

    global $wpdb;
    $wpdb->insert(
        $wpdb->prefix . 'wsal_occurrences',
        array(
            'site_id' => '1',
            'alert_id' => $alert_id,
            'created_on' => time(),
            'is_read' => 0,
            'is_migrated' => 0
        )
    );

    $eventID = $wpdb->insert_id;

    foreach ($args as $key => $value) {
        $wpdb->insert(
            $wpdb->prefix . 'wsal_metadata',
            array(
                'occurrence_id' => $eventID,
                'name' => $key,
                'value' => $value
            )
        );
    }
}

