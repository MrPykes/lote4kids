function get_wp_aiovg_videos_per_category(WP_REST_Request $param)
{
    global $post;
    $args = array();
    // $paged = (get_query_var('paged')) ? get_query_var('paged') : 1;
    $args = array(
        'post_type' => 'aiovg_videos',
        'post_status' => 'publish',
        'posts_per_page' => -1,
        'tax_query' => array(
            'relation' => 'AND',
            array(
                'taxonomy' => 'aiovg_categories',
                'field'    => 'id',
                'terms'    =>  $param['id'],
            ),
        ),
    );
    if (isset($_GET) && $_GET['sort']) {
        switch ($_GET['sort']) {
            case 'latest-release':
                $args['orderby'] = 'post_date';
                $args['order'] = 'DESC';
                break;
            case 'most-popular':
                $args['orderby'] = 'filter-views';
                $args['order'] = 'DESC';
                $args['meta_query'] = array(
                    'filter-views' => array(
                        'key' => 'views',
                        'type' => 'NUMERIC'
                    )
                );
                break;
            case 'a-z':
                $args['orderby'] = 'post_name';
                $args['order'] = 'ASC';
                break;
            case 'z-a':
                $args['orderby'] = 'post_name';
                $args['order'] = 'DESC';
                break;
            default:
                break;
        }
    }
    $query = new WP_Query($args);
    $data = array();
    if ($query->have_posts()) {
        while ($query->have_posts()) {
            $query->the_post();
            $filterTag = explode(", ", get_field('filter_tags', get_the_ID()));
            if (in_array($_GET['filter'], $filterTag)) {
                $data[] = array(
                    'id' => get_the_ID(),
                    'title' => $post->post_title,
                    'image' => get_post_meta(get_the_ID(), 'image', true) ? get_post_meta(get_the_ID(), 'image', true) : "",
                    'filter_tags' => $filterTag,
                );
            } elseif (isset($_GET['filter']) && $_GET['filter'] == "all") {
                $data[] = array(
                    'id' => get_the_ID(),
                    'title' => $post->post_title,
                    'image' => get_post_meta(get_the_ID(), 'image', true) ? get_post_meta(get_the_ID(), 'image', true) : "",
                    'filter_tags' => $filterTag,
                );
            } elseif (!isset($_GET['filter'])) {
                $data[] = array(
                    'id' => get_the_ID(),
                    'title' => $post->post_title,
                    'image' => get_post_meta(get_the_ID(), 'image', true) ? get_post_meta(get_the_ID(), 'image', true) : "",
                    'filter_tags' => $filterTag,
                );
            }
        }
    }
    return $data;
}
